S=input('Digite algo: ')
print('O Tipo primitivo desse valor é: ', type(S))
print('O TEXTO DIGITADO só tem espaços? ', S.isspace())
print('O TEXTO DIGITADO é alphabetico? ', S.isalpha()) #
print('O TEXTO DIGITADO é alphanumérico? ', S.isalnum())
print('O TEXTO DIGITADO só tem letras MAIUSCULAS? ', S.isupper())
print('O TEXTO DIGITADO só tem letras minusculas? ', S.islower())
print('O TEXTO DIGITADO só tem letras Capitalizadas? ', S.istitle())

